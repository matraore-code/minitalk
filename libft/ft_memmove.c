#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	if ((int)n == 0)
		return (dest);
	if (dest == NULL && src == NULL)
		return (NULL);
	if (src <= dest && dest < src + n)
	{
		while (n > 0)
		{
			((unsigned char *)dest)[n - 1] = ((unsigned char *)src)[n - 1];
			n--;
		}
	}
	return (ft_memcpy(dest, src, n));
}
